# Operasi Perbandingan (Boolean)

# kurang dari
a = 2
b = 3
c = a < b
print(c)

# lebih dari
a = 2
b = 3
c = a > b
print(f"a > b = {c}")

# kurang dari sama dengan
a = 2
b = 2
c = a <= b
print(f"a <= b = {c}")

# lebih dari sama dengan
a = 2
b = 3
c = a >= b
print(f"a >= b = {c}")

# sama dengan
a = 2
b = 3
c = a == b
print(f"a == b = {c}")

# tidak sama dengan sama dengan
a = 2
b = 3
c = a != b
print(f"a != b = {c}")